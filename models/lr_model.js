/**
 * Aplica imputación (mediana), escalado estándar y regresión logística.
 * @param {number[]} features - Array de 22 atributos clínicos
 * @returns {{ surgery: number, noSurgery: number }} - Probabilidades
 */
function lrPredict(features) {
  const p = LR_PARAMS;

  // 1. Imputar valores faltantes con la mediana de entrenamiento
  const x = features.map((v, i) => (isNaN(v) || v === null) ? p.medians[i] : v);

  // 2. Estandarizar: z = (x - mean) / std
  const xs = x.map((v, i) => (v - p.mean[i]) / p.scale[i]);

  // 3. Combinación lineal + sesgo (logit)
  let logit = p.intercept;
  for (let i = 0; i < xs.length; i++) logit += xs[i] * p.coef[i];

  // 4. Función sigmoide → probabilidad
  const probSurgery = 1 / (1 + Math.exp(-logit));

  return {
    surgery: probSurgery,
    noSurgery: 1 - probSurgery
  };
}
